# User input data
x = input("Please, give x value")

# Check if given data is bigger or smaller than 0
if int(x) >= 0: # If x is bigger than 0
    print(x) # We printing, because we are outside the function
else: # If not
    result = - int(x) # - Change to opposite number
    print(result) # We printing, because we are outside the function